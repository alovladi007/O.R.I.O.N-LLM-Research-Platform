        spec = dos_spec(self._STRUCT, dos_delta_e_ev=0.02)
        assert len(spec.steps) == 2
        order = toposort_steps(spec)
        assert order == ["relax", "dos"]
        dos_step = next(s for s in spec.steps if s.id == "dos")
        assert dos_step.inputs["dos_delta_e_ev"] == 0.02

