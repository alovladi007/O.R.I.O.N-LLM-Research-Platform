class TestParsePhonons:
    def test_phonons_gamma_parsed(self):
        from backend.common.engines.qe_run import parse_ph_output

        p = parse_ph_output(FIXTURES / "si_phonons_gamma.out")
        # Si primitive: 2 atoms × 3 = 6 modes total.
        assert len(p.frequencies_cm1) == 6

    def test_si_optical_mode_frequency(self):
        """Si Raman-active optical mode at Γ is ~520 cm⁻¹ experimentally;
        PBE underpredicts it by ~2% to ~509 cm⁻¹.
        """
        from backend.common.engines.qe_run import parse_ph_output

        p = parse_ph_output(FIXTURES / "si_phonons_gamma.out")
        # Last three modes are degenerate optical phonons.
        optical = sorted(p.frequencies_cm1)[-3:]
        # All three should agree within 1 cm⁻¹ (degenerate).
        assert max(optical) - min(optical) < 1.0
        # And should be in the 500–530 cm⁻¹ window for any reasonable
        # functional.
        assert 490 < optical[0] < 540

    def test_acoustic_modes_near_zero(self):
        """Three acoustic modes at Γ should be numerically ≈ 0 cm⁻¹.
        Real runs show a few-cm⁻¹ residual from finite-grid effects;
        we check < 20 cm⁻¹ to catch gross miscomputation but not
        flag normal numerical noise.
        """
        from backend.common.engines.qe_run import parse_ph_output

        p = parse_ph_output(FIXTURES / "si_phonons_gamma.out")
        acoustic = sorted(p.frequencies_cm1)[:3]
        for f in acoustic:
            assert abs(f) < 20.0

    def test_no_imaginary_modes_for_stable_si(self):
        from backend.common.engines.qe_run import parse_ph_output

        p = parse_ph_output(FIXTURES / "si_phonons_gamma.out")
        # Si at its equilibrium lattice has zero imaginary modes.
        # (Imaginary modes would mean dynamically unstable.)
        assert p.n_imaginary == 0


