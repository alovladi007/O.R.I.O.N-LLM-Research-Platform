class TestDOSHeuristics:
    def test_estimate_vbm_cbm_returns_sensible_for_insulator_gap(self):
        from src.worker.tasks import _estimate_vbm_cbm
        from backend.common.engines.qe_run.output import ParsedDOS

        # Insulator-like DOS: filled valence, gap 1 eV, empty conduction.
        dos = ParsedDOS(
            fermi_energy_ev=0.0,
            energies_ev=[-2.0, -1.0, 0.0, 1.0, 2.0],
            dos=[10.0, 10.0, 0.0, 10.0, 10.0],
            idos=[0.0, 5.0, 10.0, 15.0, 20.0],
        )
        vbm, cbm = _estimate_vbm_cbm(dos)
        assert vbm is not None and cbm is not None
        assert vbm <= 0.0
        assert cbm >= 0.5
        assert cbm - vbm > 0.5

    def test_estimate_vbm_cbm_none_if_fermi_missing(self):
        from src.worker.tasks import _estimate_vbm_cbm
        from backend.common.engines.qe_run.output import ParsedDOS

        dos = ParsedDOS(
            fermi_energy_ev=None,
            energies_ev=[-1.0, 0.0, 1.0],
            dos=[1.0, 0.0, 1.0],
            idos=[0.0, 0.5, 1.0],
        )
        assert _estimate_vbm_cbm(dos) == (None, None)
