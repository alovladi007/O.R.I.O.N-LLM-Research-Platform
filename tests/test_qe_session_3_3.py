class TestParseDOS:
    def test_dos_parsed(self):
        from backend.common.engines.qe_run import parse_dos_output

        d = parse_dos_output(FIXTURES / "si.dos")
        assert len(d.energies_ev) > 100
        assert d.fermi_energy_ev is not None
        # Real Si E_F lies ~5–7 eV.
        assert 5.0 < d.fermi_energy_ev < 8.0

    def test_integrated_dos_at_fermi_equals_electron_count(self):
        """The roadmap's DOS acceptance: ∫DOS up to E_F equals the
        electron count within 2%. Si has 8 valence electrons per
        primitive cell.
        """
        from backend.common.engines.qe_run import parse_dos_output

        d = parse_dos_output(FIXTURES / "si.dos")
        # Use the file's integrated-DOS column directly — this is
        # what dos.x writes as its own running integral.
        idx = next(i for i, e in enumerate(d.energies_ev) if e >= d.fermi_energy_ev)
        electrons_at_fermi = d.idos[idx]
        assert abs(electrons_at_fermi - 8.0) / 8.0 < 0.02, (
            f"∫DOS(E_F) = {electrons_at_fermi}, expected 8 ± 2%"
        )

    def test_parse_dos_raises_on_missing_file(self, tmp_path: Path):
        from backend.common.engines.qe_run import (
            PWOutputParseError,
            parse_dos_output,
        )

        with pytest.raises(PWOutputParseError):
            parse_dos_output(tmp_path / "nonexistent.dos")

    def test_integrate_up_to_matches_file_idos(self):
        """The trapezoidal helper should roughly agree with dos.x's own
        integration at the Fermi energy.
        """
        from backend.common.engines.qe_run import parse_dos_output

        d = parse_dos_output(FIXTURES / "si.dos")
        ours = d.integrate_up_to(d.fermi_energy_ev)
        idx = next(i for i, e in enumerate(d.energies_ev) if e >= d.fermi_energy_ev)
        theirs = d.idos[idx]
        # Trapezoidal vs Simpson + grid — 5% is plenty.
        assert abs(ours - theirs) / max(theirs, 1e-6) < 0.05


