class TestQEWorkflowTemplates:
    _STRUCT = "00000000-0000-0000-0000-000000000abc"

    def test_relax_then_static_spec_shape(self):
        from backend.common.workflows.templates.qe import relax_then_static_spec
        from backend.common.workflows import toposort_steps

        spec = relax_then_static_spec(self._STRUCT)
        assert spec.name == "relax_then_static"
        assert len(spec.steps) == 2
        kinds = {s.kind for s in spec.steps}
        assert kinds == {"dft_relax", "dft_static"}
        # scf depends on relax (via the uses ref in static_overrides).
        order = toposort_steps(spec)
